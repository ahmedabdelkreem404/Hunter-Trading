export function getSettingValue(settings = {}, key = '') {
  const value = settings?.[key]
  if (value && typeof value === 'object' && 'value' in value) {
    return value.value ?? ''
  }
  return value ?? ''
}

export function isSocialEnabled(settings = {}, enabledKey = '', url = '') {
  const raw = getSettingValue(settings, enabledKey)
  if (raw === '' || raw === null || raw === undefined) {
    return Boolean(String(url || '').trim())
  }

  return raw === true || raw === 1 || ['1', 'true', 'yes', 'on'].includes(String(raw).toLowerCase())
}

export function buildSocialLinksFromSettings(settings = {}) {
  return SOCIAL_PLATFORMS
    .map((item) => {
      const url = String(getSettingValue(settings, item.urlKey) || '').trim()
      return {
        ...item,
        label: item.name,
        url,
        is_enabled: isSocialEnabled(settings, item.enabledKey, url),
      }
    })
    .filter((item) => item.is_enabled && item.url)
    .sort((a, b) => Number(a.order || 0) - Number(b.order || 0))
}

export function getSocialBrand(platform = '') {
  const key = String(platform).trim().toLowerCase()
  const inferredKey =
    brands[key] ? key :
    key.includes('telegram') || key.includes('t.me/') ? 'telegram' :
    key.includes('instagram') ? 'instagram' :
    key.includes('youtube') || key.includes('youtu.be') ? 'youtube' :
    key.includes('tiktok') ? 'tiktok' :
    key.includes('facebook') || key.includes('fb.com') ? 'facebook' :
    key === 'x' || key.includes('twitter') || key.includes('x.com/') ? 'x' :
    key.includes('whatsapp') || key.includes('wa.me/') ? 'whatsapp' :
    key.includes('linkedin') ? 'linkedin' :
    ''

  return brands[inferredKey] || {
    label: platform || 'Website',
    color: 'var(--accent-primary)',
    background: 'rgba(255, 255, 255, 0.06)',
    border: 'rgba(255, 255, 255, 0.12)',
    Icon: Globe,
  }
}

export function normalizeSocialUrl(url = '') {
  const trimmed = String(url).trim()
  if (!trimmed) return ''
  if (/^(https?:|mailto:|tel:|sms:|whatsapp:|tg:|#|\/)/i.test(trimmed)) return trimmed
  return `https://${trimmed}`
}

export default function SocialBrandIcon({ platform, className = 'h-5 w-5' }) {
  const brand = getSocialBrand(platform)
  const Icon = brand.Icon

  return <Icon className={className} />
}
